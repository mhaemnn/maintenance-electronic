# Belajar operasi aritmatika


# operasi tambah +
a = 10
b = 20
hasil = a + b
print(a, '+', b, '=', hasil)

# operasi pengurangan -
hasil = a - b
print(a, '-', b, '=', hasil)

# operasi perkalian *
hasil = a * b
print(a, '*', b, '=', hasil)

# operasi pengurangan /
hasil = a / b
print(a, '/', b, '=', hasil)

# operasi ekspnen (pangkat)
hasil = a ** b
print(a, '**', b, '=', hasil)

# operasai modulus % (hasil bagi)
hasil = a % b
print(a, '%', b, '=', hasil)

# operasi floor devision (pembulatan)
hasil = a // b
print(a, '//', b, '=', hasil)


